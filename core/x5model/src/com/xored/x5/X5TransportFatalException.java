package com.xored.x5;

public class X5TransportFatalException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public X5TransportFatalException() {
		super();
	}

	public X5TransportFatalException(String message, Throwable cause) {
		super(message, cause);
	}

	public X5TransportFatalException(String message) {
		super(message);
	}

	public X5TransportFatalException(Throwable cause) {
		super(cause);
	}

}
